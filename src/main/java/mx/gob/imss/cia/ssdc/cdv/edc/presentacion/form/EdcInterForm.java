package mx.gob.imss.cia.ssdc.cdv.edc.presentacion.form;

import org.springframework.beans.factory.annotation.Qualifier;
import org.springframework.context.annotation.Scope;
import org.springframework.stereotype.Component;

@Component
@Qualifier("edcInterForm")
@Scope("session")
public class EdcInterForm {
	
	
	private Integer datoAnterior;
	private Integer datoPosterior;
	/**
	 * @return the datoAnterior
	 */
	public Integer getDatoAnterior() {
		return datoAnterior;
	}
	/**
	 * @param datoAnterior the datoAnterior to set
	 */
	public void setDatoAnterior(Integer datoAnterior) {
		this.datoAnterior = datoAnterior;
	}
	/**
	 * @return the datoPosterior
	 */
	public Integer getDatoPosterior() {
		return datoPosterior;
	}
	/**
	 * @param datoPosterior the datoPosterior to set
	 */
	public void setDatoPosterior(Integer datoPosterior) {
		this.datoPosterior = datoPosterior;
	}
	
	

}
