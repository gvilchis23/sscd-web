package mx.gob.imss.cia.ssdc.cdv.edc.presentacion.form;

import java.io.Serializable;

import org.springframework.beans.factory.annotation.Qualifier;
import org.springframework.context.annotation.Scope;
import org.springframework.stereotype.Component;

/**
 * @author propietario
 *
 */

@Component
@Qualifier("edcEncabezadoForm")
@Scope("session")
public class EdcEncabezadoForm implements Serializable  {
	
	private static final long serialVersionUID = 1L;
	private String turno;
	private String unidad;
	private String ventanilla;
	private String delegacion;
	/**
	 * @return the turno
	 */
	public String getTurno() {
		return turno;
	}
	/**
	 * @param turno the turno to set
	 */
	public void setTurno(String turno) {
		this.turno = turno;
	}
	/**
	 * @return the unidad
	 */
	public String getUnidad() {
		return unidad;
	}
	/**
	 * @param unidad the unidad to set
	 */
	public void setUnidad(String unidad) {
		this.unidad = unidad;
	}
	/**
	 * @return the ventanilla
	 */
	public String getVentanilla() {
		return ventanilla;
	}
	/**
	 * @param ventanilla the ventanilla to set
	 */
	public void setVentanilla(String ventanilla) {
		this.ventanilla = ventanilla;
	}
	/**
	 * @return the delegacion
	 */
	public String getDelegacion() {
		return delegacion;
	}
	/**
	 * @param delegacion the delegacion to set
	 */
	public void setDelegacion(String delegacion) {
		this.delegacion = delegacion;
	}
	
	

}
